"""
Knowledge-base tools for the co-worker agent.

`search_knowledge` is TF-IDF retrieval over the per-case wiki chunk index.
`read_note` looks up a specific note (by source_id + note_ref) via
`wiki/note_links.json` and returns its markdown plus the rows it backs.
"""

from __future__ import annotations

import json
import re
from typing import Any, Dict, List, Optional

from core.cases.case_store import CaseStore
from core.knowledge import search_case_wiki


_store = CaseStore()


def _note_key(value: Any) -> str:
    raw = str(value or "").strip().lower().replace("note", "").strip()
    raw = re.sub(r"[^a-z0-9.]+", "", raw)
    return raw[:-2] if raw.endswith(".0") else raw


SEARCH_KNOWLEDGE_SPEC: Dict[str, Any] = {
    "name": "search_knowledge",
    "description": (
        "Search the case knowledge base — the per-source wiki pages, notes, "
        "narrative sections (auditor / directors), merged statements, "
        "analytics summary, and credit memo — and return the top-K chunks "
        "ranked by relevance. Each hit has a snippet, source PDF, page range, "
        "and a wiki path you can quote. Use this when the analyst asks about "
        "qualitative content (notes, accounting policies, auditor comments, "
        "related-party disclosures, going concern, etc.) or anything that "
        "isn't a structured ratio."
    ),
    "input_schema": {
        "type": "object",
        "properties": {
            "query": {
                "type": "string",
                "description": "Natural-language query. Single keywords also work.",
            },
            "k": {
                "type": "integer",
                "description": "Max number of chunks to return. Default 5, max 12.",
                "minimum": 1,
                "maximum": 12,
            },
        },
        "required": ["query"],
    },
}


def search_knowledge(case_id: str, query: str, k: int = 5) -> Dict[str, Any]:
    case_root = _store._case_path(case_id)
    wiki_root = case_root / "wiki"
    if not wiki_root.exists():
        return {
            "result": {
                "available": False,
                "reason": (
                    "Case knowledge base has not been built yet. The wiki is "
                    "generated by the analysis pipeline — analyst must run "
                    "/analyze (or POST /cases/{id}/knowledge/rebuild)."
                ),
            },
            "citations": [],
        }

    k = max(1, min(int(k or 5), 12))
    raw = search_case_wiki(case_root, query, limit=k)
    hits: List[Dict[str, Any]] = raw.get("results") or []

    result = {
        "available": True,
        "query": raw.get("query", query),
        "count": len(hits),
        "hits": [
            {
                "title": h.get("title"),
                "doc_type": h.get("doc_type"),
                "snippet": h.get("snippet"),
                "score": h.get("score"),
                "source_id": h.get("source_id"),
                "source_file": h.get("source_file"),
                "page_range": h.get("page_range"),
                "wiki_path": h.get("path"),
            }
            for h in hits
        ],
    }

    citations: List[Dict[str, Any]] = [
        {
            "kind": "wiki",
            "title": h.get("title"),
            "wiki_path": h.get("path"),
            "source_id": h.get("source_id"),
            "source_file": h.get("source_file"),
            "page_range": h.get("page_range"),
        }
        for h in hits
    ]
    return {"result": result, "citations": citations}


